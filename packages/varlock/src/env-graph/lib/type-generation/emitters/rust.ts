import { getAutogeneratedBanner } from '../banners';
import {
  getFieldDocLines,
  getSensitiveKeys,
  type CoercedType,
  type ResolvedFieldType,
} from '../shared';

function getRustCoercedTypeString(coerced: CoercedType): string {
  if (coerced === 'int') return 'i64';
  if (coerced === 'number') return 'f64';
  if (coerced === 'boolean') return 'bool';
  if (coerced === 'object') return 'serde_json::Value';
  // Rust has no string-literal/enum types, so enums are plain Strings
  return 'String';
}

function envKeyToRustFieldName(key: string): string {
  return key.toLowerCase();
}

function fieldDocComment(field: ResolvedFieldType): Array<string> {
  return getFieldDocLines(field).map((line) => `    /// ${line.replace(/\r?\n/g, ' ')}`);
}

export function generateRustEnvSrc(fields: Array<ResolvedFieldType>): string {
  const sensitiveKeys = getSensitiveKeys(fields);

  const structFields = fields.flatMap((field) => {
    const baseType = getRustCoercedTypeString(field.coerced);
    const rustType = field.isRequired ? baseType : `Option<${baseType}>`;
    return [
      ...fieldDocComment(field),
      `    pub ${envKeyToRustFieldName(field.key)}: ${rustType},`,
    ];
  });

  const src = [
    getAutogeneratedBanner('//'),
    '',
    'use serde::Deserialize;',
    '',
    '/// Coerced env schema (matches varlock coercion).',
    '#[derive(Debug, Clone, Deserialize)]',
    '#[serde(rename_all = "SCREAMING_SNAKE_CASE")]',
    'pub struct Env {',
    ...structFields,
    '}',
    '',
    '/// Keys whose values are sensitive — use to build your own redaction / leak-scanning.',
    `pub const SENSITIVE_KEYS: &[&str] = &[${sensitiveKeys.map((k) => JSON.stringify(k)).join(', ')}];`,
    '',
    '/// Parse the `__VARLOCK_ENV` blob (injected by `varlock run`) into a typed, coerced `Env`.',
    'pub fn load() -> Result<Env, Box<dyn std::error::Error>> {',
    '    let graph: serde_json::Value = serde_json::from_str(&std::env::var("__VARLOCK_ENV")?)?;',
    '    let mut flat = serde_json::Map::new();',
    '    if let Some(config) = graph.get("config").and_then(|c| c.as_object()) {',
    '        for (key, entry) in config {',
    '            if let Some(value) = entry.get("value") {',
    '                flat.insert(key.clone(), value.clone());',
    '            }',
    '        }',
    '    }',
    '    Ok(serde_json::from_value(serde_json::Value::Object(flat))?)',
    '}',
    '',
  ];

  return src.join('\n');
}
