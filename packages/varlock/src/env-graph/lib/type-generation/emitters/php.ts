import { getAutogeneratedBanner } from '../banners';
import {
  type CoercedType,
  type RawStringType,
  type ResolvedFieldType,
} from '../shared';

function phpStringLiteralType(value: string): string {
  return JSON.stringify(value);
}

function formatPhpEnumLiteral(option: string | number | boolean): string {
  if (typeof option === 'string') return phpStringLiteralType(option);
  if (typeof option === 'boolean') return option ? "'true'" : "'false'";
  return String(option);
}

function getPhpCoercedTypeString(coerced: CoercedType): string {
  if (coerced === 'int') return 'int';
  if (coerced === 'number') return 'float';
  if (coerced === 'boolean') return 'bool';
  if (coerced === 'object') return 'array<string, mixed>';
  if (typeof coerced === 'object' && 'enum' in coerced) {
    if (!coerced.enum.length) return 'never';
    return coerced.enum.map(formatPhpEnumLiteral).join('|');
  }
  return 'string';
}

function getPhpRawStringTypeString(rawString: RawStringType): string {
  if (typeof rawString === 'object' && 'boolean' in rawString) {
    return "'true'|'false'";
  }
  if (typeof rawString === 'object' && 'enum' in rawString) {
    return rawString.enum.map(phpStringLiteralType).join('|');
  }
  return 'string';
}

function escapePhpDoc(text: string): string {
  return text.replace(/\*\//g, '* /');
}

function formatPhpTypeAlias(
  typeName: string,
  doc: string,
  fields: Array<ResolvedFieldType>,
  getType: (field: ResolvedFieldType) => string,
): Array<string> {
  const shapeParts: Array<string> = [];

  for (const field of fields) {
    const typeString = getType(field);
    const optionalMarker = field.isRequired ? '' : '?';
    shapeParts.push(`${field.key}${optionalMarker}: ${typeString}`);
  }

  const shape = shapeParts.length ? `{${shapeParts.join(', ')}}` : 'array{}';

  return [
    '',
    '/**',
    ` * ${escapePhpDoc(doc)}`,
    ' */',
    `/** @phpstan-type ${typeName} ${shape} */`,
  ];
}

export function generatePhpTypesSrc(fields: Array<ResolvedFieldType>): string {
  const publicFields = fields.filter((field) => !field.isSensitive);

  const src = [
    '<?php',
    getAutogeneratedBanner('//'),
    ...formatPhpTypeAlias(
      'CoercedEnvSchema',
      'Coerced env schema (matches varlock coercion).',
      fields,
      (field) => getPhpCoercedTypeString(field.coerced),
    ),
    ...formatPhpTypeAlias(
      'PublicCoercedEnvSchema',
      'Non-sensitive coerced env schema keys.',
      publicFields,
      (field) => getPhpCoercedTypeString(field.coerced),
    ),
    ...formatPhpTypeAlias(
      'EnvSchemaAsStrings',
      'Raw values from getenv / $_ENV (always strings).',
      fields,
      (field) => getPhpRawStringTypeString(field.rawString),
    ),
  ];

  return `${src.join('\n')}\n`;
}
