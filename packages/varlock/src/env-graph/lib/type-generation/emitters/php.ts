import _ from '@env-spec/utils/my-dash';
import { PHP_AUTOGENERATED_BANNER } from '../banners';
import {
  getPhpCoercedTypeString,
  getPhpRawStringTypeString,
  type ResolvedFieldType,
} from '../shared';

function escapePhpDoc(text: string): string {
  return text.replace(/\*\//g, '* /');
}

function formatPhpTypeAlias(
  typeName: string,
  doc: string,
  fields: Array<ResolvedFieldType>,
  getType: (field: ResolvedFieldType) => string,
): Array<string> {
  const shapeParts: Array<string> = [];

  for (const field of fields) {
    const typeString = getType(field);
    const optionalMarker = field.isRequired ? '' : '?';
    shapeParts.push(`${field.key}${optionalMarker}: ${typeString}`);
  }

  const shape = shapeParts.length ? `{${shapeParts.join(', ')}}` : 'array{}';

  return [
    '',
    '/**',
    ` * ${escapePhpDoc(doc)}`,
    ' */',
    `/** @phpstan-type ${typeName} ${shape} */`,
  ];
}

export function generatePhpTypesSrc(fields: Array<ResolvedFieldType>): string {
  const publicFields = fields.filter((field) => !field.isSensitive);

  const src = [
    PHP_AUTOGENERATED_BANNER,
    ...formatPhpTypeAlias(
      'CoercedEnvSchema',
      'Coerced env schema (matches varlock coercion).',
      fields,
      (field) => getPhpCoercedTypeString(field.coerced),
    ),
    ...formatPhpTypeAlias(
      'PublicCoercedEnvSchema',
      'Non-sensitive coerced env schema keys.',
      publicFields,
      (field) => getPhpCoercedTypeString(field.coerced),
    ),
    ...formatPhpTypeAlias(
      'EnvSchemaAsStrings',
      'Raw values from getenv / $_ENV (always strings).',
      fields,
      (field) => getPhpRawStringTypeString(field.rawString),
    ),
  ];

  return `${src.join('\n')}\n`;
}
