/** SCREAMING_SNAKE / snake → PascalCase (`DB_HOST` → `DbHost`). */
export function snakeToPascalCase(key: string): string {
  return key
    .split('_')
    .filter((p) => p.length > 0)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1).toLowerCase())
    .join('');
}

